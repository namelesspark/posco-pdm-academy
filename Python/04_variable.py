# ========================================
print("===== 여러 변수 한번에 선언 및 할당 =====")
width, height = 10, 20  # width는 10, height는 20이 할당
print("width", width, "height", height)
# 형식: 변수1, 변수2가 있다면, 값도 여러개를 쓸 수 있다.
# 중요한 건 값의 개수와 변수의 개수가 같아야 한다.

# x, y, z = 36, 38 -> z값이 할당되지 않았으므로 오류가 발생한다.


