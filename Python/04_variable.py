# 변수 선언 방법
# 변수 이름 = 값
# 오른쪽 값을 왼쪽 이름에 "저장"하라는 코드

temp = 36  # 숫자로 저장하고 싶다면 수치만
name = "센서 A"  # 문자로 저장하고 싶다면 따옴표로 감싸기

print(temp)
print(name)

# = 은 같다가 아니라 저장 하는 것
# 비교는 == 연산자 사용 (비교 연산자)

# ========================================
print("===== 변수 사용 활용 =====")

x = 5
print(x)
x = x + 5  # 변수를 재할당할 때
print(x)

# y = y + 5  # 이렇게 사용하면, 오류가 뜬다. y가 먼저 선언되지 않았기 때문

# ========================================
print("===== 재할당 =====")
temp = 15  # 이 선언 전, 위에서 temp는 36이었다!
Temp = 150  # temp와 다른 변수로 동작하는 "대소문자 구분"
print(temp)
temp = temp + 20
print(temp)
temp += 20
print(temp)

print(Temp)

score = 10
score = 20
