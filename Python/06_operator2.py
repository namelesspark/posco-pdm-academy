print("\n===== 문자열 연산 =====")
print("안녕" + "하세요")  # 안녕하세요

# 만약 안녕과 하세요 사이에 공백을 1개 넣고 싶다면
# 방법 1: , 사용
print("안녕", "하세요")

# 방법 2: 안녕 뒤에 공백 추가
print("안녕 " + "하세요")

# 방볍 3: 공백만 있는 문자열 더하기
print("안녕" + " " + "하세요")


