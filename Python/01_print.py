# 실습

print(f"오늘 날짜 숫자로 출력: > {20260714}")  # 오늘 날짜 숫자로 출력
print(f"오늘 날짜 문자로 출력: > {"2026년 07월 14일"}")  # 오늘 날짜 문자로 출력
print(f"내 생일 '숫자'로 출력: > {20011028}")  # 내 생일 숫자로 출력

print(0, 123)

# ====================================================

